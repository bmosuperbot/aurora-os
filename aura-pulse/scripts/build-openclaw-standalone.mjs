import { cpSync, existsSync, mkdirSync, readFileSync, rmSync, symlinkSync, writeFileSync } from 'node:fs'
import { execFileSync } from 'node:child_process'
import { dirname, join, relative, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const scriptDir = dirname(fileURLToPath(import.meta.url))
const auraPulseRoot = resolve(scriptDir, '..')
const outputDir = join(auraPulseRoot, 'dist', 'openclaw-plugin-standalone')
const pluginDir = join(auraPulseRoot, 'packages', 'openclaw-plugin')
const runtimeDir = join(auraPulseRoot, 'packages', 'contract-runtime')
const artistResellerDir = join(auraPulseRoot, 'packages', 'artist-reseller')

function writeBundlePackageJson() {
    const source = JSON.parse(readFileSync(join(pluginDir, 'package.json'), 'utf8'))
    const bundlePkg = {
        ...source,
        private: false,
        description: `${source.description ?? 'Aura Pulse OpenClaw plugin'} (standalone bundle)`,
        scripts: {},
    }

    writeFileSync(join(outputDir, 'package.json'), JSON.stringify(bundlePkg, null, 2) + '\n')
}

function writeBundleReadme() {
    const lines = [
        '# Aura Pulse standalone bundle',
        '',
        'Generated by `aura-pulse/scripts/build-openclaw-standalone.mjs`.',
        '',
        'Contents:',
        '',
        '- self-contained OpenClaw plugin root',
        '- vendored `artist-reseller` package assets under `vendor/artist-reseller/`',
        '- vendored `contract-runtime` package assets under `vendor/contract-runtime/`',
        '- copied `node_modules/` from the workspace plugin package',
        '',
        'Expected OpenClaw load path:',
        '',
        '- host: `aura-pulse/dist/openclaw-plugin-standalone`',
        '- container: `/workspaces/aura-pulse/dist/openclaw-plugin-standalone`',
    ]

    writeFileSync(join(outputDir, 'README.md'), lines.join('\n') + '\n')
}

function ensureSelfAlias() {
    const auraScopeDir = join(outputDir, 'node_modules', '@aura')
    mkdirSync(auraScopeDir, { recursive: true })

    const aliasPath = join(auraScopeDir, 'aura-pulse')
    rmSync(aliasPath, { recursive: true, force: true })

    const target = relative(dirname(aliasPath), outputDir)
    symlinkSync(target, aliasPath, 'dir')
}

function ensureWorkspaceInstalled() {
    if (!existsSync(join(pluginDir, 'node_modules'))) {
        throw new Error('packages/openclaw-plugin/node_modules is missing; run pnpm install in aura-pulse first')
    }
}

function deployPortablePlugin() {
    execFileSync(
        'pnpm',
        ['--filter', '@aura/aura-pulse', 'deploy', '--prod', '--legacy', outputDir],
        { cwd: auraPulseRoot, stdio: 'inherit' },
    )
}

ensureWorkspaceInstalled()

rmSync(outputDir, { recursive: true, force: true })
deployPortablePlugin()
mkdirSync(join(outputDir, 'vendor', 'contract-runtime'), { recursive: true })
mkdirSync(join(outputDir, 'vendor', 'artist-reseller'), { recursive: true })

cpSync(join(runtimeDir, 'package.json'), join(outputDir, 'vendor', 'contract-runtime', 'package.json'))
cpSync(join(runtimeDir, 'src'), join(outputDir, 'vendor', 'contract-runtime', 'src'), { recursive: true, dereference: true })
cpSync(join(artistResellerDir, 'package.json'), join(outputDir, 'vendor', 'artist-reseller', 'package.json'))
cpSync(join(artistResellerDir, 'aurora-registry.json'), join(outputDir, 'vendor', 'artist-reseller', 'aurora-registry.json'))
cpSync(join(artistResellerDir, 'domain-types.json'), join(outputDir, 'vendor', 'artist-reseller', 'domain-types.json'))
cpSync(join(artistResellerDir, 'tools'), join(outputDir, 'vendor', 'artist-reseller', 'tools'), { recursive: true, dereference: true })
cpSync(join(artistResellerDir, 'apps'), join(outputDir, 'vendor', 'artist-reseller', 'apps'), { recursive: true, dereference: true })

writeBundlePackageJson()
writeBundleReadme()
ensureSelfAlias()

console.log(`Standalone Aura Pulse bundle written to ${outputDir}`)